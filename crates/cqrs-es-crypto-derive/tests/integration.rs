#[tokio::test]
async fn each_persist_produces_unique_ciphertext() {
    let repo = make_repo();
    let subject_id = Uuid::new_v4();

    repo.persist::<TestAggregate>(&[multi_secret_event("unique-1", 1, subject_id)], None)
        .await
        .unwrap();
    repo.persist::<TestAggregate>(&[multi_secret_event("unique-2", 1, subject_id)], None)
        .await
        .unwrap();

    let raw = repo.inner().all_events();
    let ct1 = raw[0].payload["MultiSecret"]["encrypted_pii"]
        .as_str()
        .unwrap();
    let ct2 = raw[1].payload["MultiSecret"]["encrypted_pii"]
        .as_str()
        .unwrap();

    assert_ne!(
        ct1, ct2,
        "a fresh nonce per encryption must produce distinct ciphertexts even for identical input"
    );
}

