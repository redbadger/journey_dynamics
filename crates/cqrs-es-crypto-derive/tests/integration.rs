// ── Read path: redaction (DEK deleted) ───────────────────────────────────────

/// Persist a multi-secret event, delete the subject's DEK, then read back and
/// verify that String secret fields are replaced with `"[redacted]"` while
/// plaintext fields are preserved.
#[tokio::test]
async fn multi_secret_redacts_string_fields_when_key_deleted() {
    let (repo, key_store) = make_repo_with_key_store();
    let aggregate_id = "ms-redact";
    let subject_id = Uuid::new_v4();

    repo.persist::<TestAggregate>(&[multi_secret_event(aggregate_id, 1, subject_id)], None)
        .await
        .unwrap();

    // Shred: delete the DEK so decryption is impossible.
    key_store.delete_key(&subject_id).await.unwrap();

    let events = repo
        .get_events::<TestAggregate>(aggregate_id)
        .await
        .unwrap();

    assert_eq!(events.len(), 1);
    let inner = &events[0].payload["MultiSecret"];

    // Secret String fields must be replaced with the "[redacted]" sentinel.
    assert_eq!(
        inner["name"].as_str().unwrap(),
        "[redacted]",
        "name must be redacted after key deletion"
    );
    assert_eq!(
        inner["email"].as_str().unwrap(),
        "[redacted]",
        "email must be redacted after key deletion"
    );

    // Non-PII fields must still be readable.
    assert_eq!(inner["tag"].as_str().unwrap(), "some-tag");
    assert_eq!(
        inner["subject_id"].as_str().unwrap(),
        subject_id.to_string().as_str()
    );
}

